    /// <summary>
    /// 添加钉钉api服务
    /// </summary>
    /// <param name="services"></param>
    /// <param name="configuration">配置</param>
    /// <param name="providerNames">需要注册的访问令牌 Provider 名称；为空时默认注册内部应用。</param>
    /// <returns></returns>
    public static IServiceCollection AddDingTalk(this IServiceCollection services, IConfiguration configuration, params string[] providerNames)
    {
        ArgumentNullException.ThrowIfNull(services);
        ArgumentNullException.ThrowIfNull(configuration);

        var enableLogging = ParseBoolean(configuration["DingTalk:EnableLogging"]);
        services.Configure<DingTalkOptions>(options =>
        {
            options.EnableLogging = enableLogging;
        });
        var providers = NormalizeProviderNames(providerNames);
        foreach (var providerName in providers)
        {
            services.Configure<DingTalkAccessTokenOptions>(providerName, options =>
            {
                var section = configuration.GetSection($"DingTalk:{providerName}");
                ConfigureIfPresent(section, nameof(options.ClientId), value => options.ClientId = value);
                ConfigureIfPresent(section, nameof(options.ClientSecret), value => options.ClientSecret = value);
                ConfigureIfPresent(section, nameof(options.SuiteKey), value => options.SuiteKey = value);
                ConfigureIfPresent(section, nameof(options.SuiteSecret), value => options.SuiteSecret = value);
                ConfigureIfPresent(section, nameof(options.AuthCorpId), value => options.AuthCorpId = value);
                ConfigureIfPresent(section, nameof(options.SuiteTicket), value => options.SuiteTicket = value);
                ConfigureIfPresent(section, nameof(options.CropId), value => options.CropId = value);
                ConfigureIfPresent(section, nameof(options.SsoSecret), value => options.SsoSecret = value);
            });
        }

        if (providers.Contains(ProviderNames.Internal, StringComparer.OrdinalIgnoreCase))
        {
            services.AddTokenProvider<IDingTalkApiClient, AccessTokenProvider>(ProviderNames.Internal);
        }

        if (providers.Contains(ProviderNames.Corp, StringComparer.OrdinalIgnoreCase))
        {
            services.AddTokenProvider<IDingTalkApiClient, CropAccessTokenProvider>(ProviderNames.Corp);
        }

        if (providers.Contains(ProviderNames.MicroApp, StringComparer.OrdinalIgnoreCase))
        {
            services.AddTokenProvider<IDingTalkApiClient, SsoAccessTokenProvider>(ProviderNames.MicroApp);
        }

        services.AddWebApiClient().ConfigureHttpApi(o =>
        {
            if (o.JsonSerializeOptions is not null)
            {
                o.JsonSerializeOptions.PropertyNamingPolicy = JsonNamingPolicy.CamelCase;
                o.JsonSerializeOptions.DefaultIgnoreCondition = JsonIgnoreCondition.WhenWritingNull;
                o.JsonSerializeOptions.TypeInfoResolver = DingTalkJsonSerializerContext.Default;
                o.JsonSerializeOptions.Encoder = JavaScriptEncoder.Create(
                UnicodeRanges.BasicLatin,//基础拉丁文字母
                UnicodeRanges.Latin1Supplement, //拉丁字母补充
                UnicodeRanges.CjkUnifiedIdeographs, //中日韩统一的表意文字
                UnicodeRanges.CjkSymbolsandPunctuation, //中日韩标点符号
                UnicodeRanges.HalfwidthandFullwidthForms, //半角全角符号
                UnicodeRanges.MathematicalOperators, //数学运算符号
                UnicodeRanges.SuperscriptsandSubscripts,//上下标
                UnicodeRanges.GeneralPunctuation, //一般标点符号
                UnicodeRanges.CurrencySymbols, //货币符号
                UnicodeRanges.GeometricShapes //几何图形
                );
            }

            if (o.JsonDeserializeOptions is not null)
            {
                o.JsonDeserializeOptions.PropertyNamingPolicy = JsonNamingPolicy.CamelCase;
                o.JsonDeserializeOptions.TypeInfoResolver = DingTalkJsonSerializerContext.Default;
            }

            o.KeyValueSerializeOptions?.PropertyNamingPolicy = JsonNamingPolicy.CamelCase;
        });

        AddConfiguredHttpApi<IAccessTokenApi>(services, enableLogging);
        AddConfiguredHttpApi<IBadgeApi>(services, enableLogging);
        AddConfiguredHttpApi<IVideoConferencesApi>(services, enableLogging);
        AddConfiguredHttpApi<ILiveApi>(services, enableLogging);
        AddConfiguredHttpApi<IMeetingRoomsApi>(services, enableLogging);
        AddConfiguredHttpApi<IRobotApi>(services, enableLogging);
        AddConfiguredHttpApi<ICorpConversationApi>(services, enableLogging);
        AddConfiguredHttpApi<IChatApi>(services, enableLogging);
        AddConfiguredHttpApi<ISceneGroupApi>(services, enableLogging);
        AddConfiguredHttpApi<IInteractiveCardApi>(services, enableLogging);
        AddConfiguredHttpApi<IInterconnectionsApi>(services, enableLogging);
        AddConfiguredHttpApi<ICalendarApi>(services, enableLogging);
        AddConfiguredHttpApi<ICardInstancesApi>(services, enableLogging);
        AddConfiguredHttpApi<IUserManageApi>(services, enableLogging, o =>
        {
            if (o.JsonSerializeOptions is not null)
            {
                o.JsonSerializeOptions.PropertyNamingPolicy = JsonNamingPolicy.SnakeCaseLower;
                o.JsonSerializeOptions.DefaultIgnoreCondition = JsonIgnoreCondition.WhenWritingNull;
            }

            o.JsonDeserializeOptions?.PropertyNamingPolicy = JsonNamingPolicy.SnakeCaseLower;
            o.KeyValueSerializeOptions?.PropertyNamingPolicy = JsonNamingPolicy.SnakeCaseLower;
        });
        AddConfiguredHttpApi<IEmployeeRecordsApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<ISeniorSettingsApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IStaffAttributesApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IOrgAccountsApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IDepartmentManageApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IRoleManageApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IExternalContactsApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IContactSettingsApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IIndustryContactsApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IAuthScopesApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IContactUsersApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<ICooperateCorpsApi>(services, enableLogging);
        AddConfiguredHttpApi<IUnionCooperateApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<ISsoAuthApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IAttendanceApprovalsApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IAttendanceGroupsApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IAttendanceMachinesApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IAttendanceMachinesV1Api>(services, enableLogging);
        AddConfiguredHttpApi<IAttendanceRecordsApi>(services, enableLogging, ConfigureSnakeCase);
