        AddConfiguredHttpApi<IWorkflowApi>(services, enableLogging);
        AddConfiguredHttpApi<IOrgCultureApi>(services, enableLogging);
        AddConfiguredHttpApi<IOrgCultureHealthApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IBlackboardApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IBlackboardV1Api>(services, enableLogging);
        AddConfiguredHttpApi<ICrmApi>(services, enableLogging);
        AddConfiguredHttpApi<ICrmTopApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IExclusiveOpenApi>(services, enableLogging);
        AddConfiguredHttpApi<IExclusiveOpenServiceAccountApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IApplicationManagementApi>(services, enableLogging);
        AddConfiguredHttpApi<ITodoApi>(services, enableLogging);
        AddConfiguredHttpApi<IWorkbenchApi>(services, enableLogging);
        AddConfiguredHttpApi<IYidaApi>(services, enableLogging);
        AddConfiguredHttpApi<IDocumentFileApi>(services, enableLogging);
        AddConfiguredHttpApi<IReportApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IServiceWindowApi>(services, enableLogging);
        AddConfiguredHttpApi<IEventSubscriptionApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<ISmartDeviceApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<ISmartFillApi>(services, enableLogging);
        AddConfiguredHttpApi<IHumanResourcesApi>(services, enableLogging);
        AddConfiguredHttpApi<IHumanResourcesTopApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IIndustryOpenApi>(services, enableLogging);
        AddConfiguredHttpApi<IIndustryOpenTopApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<IEcosystemApi>(services, enableLogging);
        AddConfiguredHttpApi<IEcosystemTopApi>(services, enableLogging, ConfigureSnakeCase);
        AddConfiguredHttpApi<ITeambitionProjectApi>(services, enableLogging);
        AddConfiguredHttpApi<ITeambitionWorkspaceTopApi>(services, enableLogging, ConfigureSnakeCase);



        return services;
    }

    private static void AddConfiguredHttpApi<
        [DynamicallyAccessedMembers(DynamicallyAccessedMemberTypes.All)] THttpApi>(
        IServiceCollection services,
        bool enableLogging,
        Action<HttpApiOptions>? configure = null)
        where THttpApi : class
    {
        services.AddHttpApi<THttpApi>(o =>
        {
            o.UseLogging = enableLogging;
            configure?.Invoke(o);
        });
    }

    private static void ConfigureSnakeCase(HttpApiOptions options)
    {
        if (options.JsonSerializeOptions is not null)
        {
            options.JsonSerializeOptions.PropertyNamingPolicy = JsonNamingPolicy.SnakeCaseLower;
            options.JsonSerializeOptions.DefaultIgnoreCondition = JsonIgnoreCondition.WhenWritingNull;
        }

        options.JsonDeserializeOptions?.PropertyNamingPolicy = JsonNamingPolicy.SnakeCaseLower;
        options.KeyValueSerializeOptions?.PropertyNamingPolicy = JsonNamingPolicy.SnakeCaseLower;
    }

    private static string[] NormalizeProviderNames(string[]? providerNames)
    {
        if (providerNames is null || providerNames.Length == 0)
        {
            return [ProviderNames.Internal];
        }

        var providers = providerNames
            .Select(providerName => providerName?.Trim())
            .Where(providerName => !string.IsNullOrWhiteSpace(providerName))
            .Select(providerName => providerName!)
            .Distinct(StringComparer.OrdinalIgnoreCase)
            .ToArray();

        if (providers.Length == 0)
        {
            return [ProviderNames.Internal];
        }

        foreach (var providerName in providers)
        {
            if (!IsSupportedProviderName(providerName))
            {
                throw new ArgumentException($"不支持的钉钉 Provider 名称: {providerName}", nameof(providerNames));
            }
        }

        return providers;
    }

    private static bool IsSupportedProviderName(string providerName)
    {
        return providerName.Equals(ProviderNames.Internal, StringComparison.OrdinalIgnoreCase)
            || providerName.Equals(ProviderNames.Corp, StringComparison.OrdinalIgnoreCase)
            || providerName.Equals(ProviderNames.MicroApp, StringComparison.OrdinalIgnoreCase);
    }

    private static void ConfigureIfPresent(IConfiguration section, string key, Action<string> configure)
    {
        var value = section[key];
        if (value is not null)
        {
            configure(value);
        }
    }

    private static bool ParseBoolean(string? value)
    {
        return bool.TryParse(value, out var result) && result;
    }
}





